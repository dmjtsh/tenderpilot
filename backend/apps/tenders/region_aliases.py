# Обратный маппинг: любой вариант → канонический
_VARIANT_TO_CANONICAL: dict[str, str] = {
    variant: canonical
    for canonical, variants in REGION_ALIASES.items()
    for variant in variants
}

# Отсортированный список канонических названий для dropdown
CANONICAL_REGIONS: list[str] = sorted(REGION_ALIASES.keys())


def expand_regions(regions: Sequence[str]) -> list[str]:
    """
    Принимает список канонических названий (из фильтра UI),
    возвращает все варианты написания для фильтрации по БД.
    Неизвестные значения пропускаются через как есть.
    """
    result: list[str] = []
    seen: set[str] = set()
    for r in regions:
        variants = REGION_ALIASES.get(r) or [r]
        for v in variants:
            if v not in seen:
                seen.add(v)
                result.append(v)
    return result
