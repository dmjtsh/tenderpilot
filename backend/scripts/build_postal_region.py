def build_canonical(region: str, region_type: str) -> str:
    """Строит канонический субъект из полей hflabs city.csv."""
    r = region.strip()
    # Сначала пробуем точное совпадение (для сложных названий)
    if r in CANONICAL_44FZ:
        return CANONICAL_44FZ[r]
    # Потом по первому слову (Московская → CANONICAL_44FZ["Московская"])
    first_word = r.split()[0] if r else ""
    if first_word in CANONICAL_44FZ:
        return CANONICAL_44FZ[first_word]
    return ""


def main() -> None:
    print(f"Загрузка {CITY_CSV_URL}...")
    with urllib.request.urlopen(CITY_CSV_URL) as resp:
        content = resp.read().decode("utf-8")

    reader = csv.DictReader(content.splitlines())
    postal_map: dict[str, str] = {}
    unmatched: set[str] = set()

    for row in reader:
        postal = (row.get("postal_code") or "").strip()
        region = (row.get("region") or "").strip()
        region_type = (row.get("region_type") or "").strip()

        if not postal or len(postal) != 6 or not postal.isdigit():
            continue
        if postal in postal_map:
            continue  # берём первый попавшийся (города в одном регионе)

        canonical = build_canonical(region, region_type)
        if canonical:
            postal_map[postal] = canonical
        else:
            unmatched.add(f"{region_type} {region}")

    # Сохраняем
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(
        json.dumps(postal_map, ensure_ascii=False, sort_keys=True, indent=None),
        encoding="utf-8",
    )
    size_kb = OUTPUT_PATH.stat().st_size // 1024
    print(f"Записано {len(postal_map)} индексов → {OUTPUT_PATH} ({size_kb} KB)")

    if unmatched:
        print(f"\nНе удалось замаппить {len(unmatched)} регионов (добавь в CANONICAL_44FZ):")
        for u in sorted(unmatched):
            print(f"  {u!r}")


if __name__ == "__main__":
    main()
